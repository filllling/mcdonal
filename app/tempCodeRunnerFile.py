# 用户表建表语句
# 字段：id（主键）、username（唯一）、password（哈希）、role（用户角色：admin/customer/analyst）

CREATE_USER_TABLE = '''
CREATE TABLE IF NOT EXISTS user (
    id INT AUTO_INCREMENT PRIMARY KEY COMMENT '用户ID',
    username VARCHAR(64) NOT NULL UNIQUE COMMENT '用户名',
    password VARCHAR(255) NOT NULL COMMENT '密码哈希',
    phone VARCHAR(20) COMMENT '手机号',
    role ENUM('admin', 'customer', 'analyst') NOT NULL DEFAULT 'customer' COMMENT '用户角色',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '注册时间'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
'''

# 菜单表建表语句
# 字段：id（主键）、name（菜品名）、price（价格）、category（分类）
CREATE_MENU_TABLE = '''
CREATE TABLE IF NOT EXISTS menu (
    id INT AUTO_INCREMENT PRIMARY KEY COMMENT '菜单项ID',
    name VARCHAR(64) NOT NULL COMMENT '菜品名称',
    price DECIMAL(8,2) NOT NULL COMMENT '价格',
    category ENUM('汉堡', '薯条', '饮料') NOT NULL COMMENT '分类'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
'''

